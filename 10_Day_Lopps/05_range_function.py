# Generates a sequence of numbers. Often used to repeat an action a specific number of times.

# Example: Print numbers 0 to 2
for i in range(3):  # range(stop) -> 0, 1, 2
    print(i)

# Example: Print even numbers from 2 to 8
for i in range(2, 10, 2):  # range(start, stop, step)
    print(i)  # Output: 2, 4, 6, 8