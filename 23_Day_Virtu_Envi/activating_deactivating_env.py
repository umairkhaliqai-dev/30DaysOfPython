# Activation modifies your shell’s PATH variable to point to the 
# environment’s binaries instead of the global Python installation. 
# Deactivation reverts these changes.

# On Windows:
# venv\Scripts\activate

# On macOS/Linux:
# source venv/bin/activate

# After activation, you should see (venv) in your terminal prompt.
# To deactivate:
# deactivate
