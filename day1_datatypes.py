# Day 1 - All Python Data Types (CORRECTED)

# 1. Integer
age = 25
print("Integer:", age, "| Type:", type(age))

# 2. Float
height = 7.75
print("Float:", height, "| Type:", type(height))

# 3. Complex
complex_num = 3 + 5j
print("Complex:", complex_num, "| Type:", type(complex_num))

# 4. String
name = "Umair Khaliq"
print("String:", name, "| Type:", type(name))

# 5. Boolean
is_student = True
print("Boolean:", is_student, "| Type:", type(is_student))

# 6. List
fruits = ["apple", "banana", "cherry"]  # Square brackets = LIST
print("List:", fruits, "| Type:", type(fruits))

# 7. Tuple
coordinates = (10, 20, 30, 40)  # Parentheses = TUPLE
print("Tuple:", coordinates, "| Type:", type(coordinates))

# 8. Set
unique_numbers = {1, 2, 3, 4, 5}  # Curly braces with values only = SET
print("Set:", unique_numbers, "| Type:", type(unique_numbers))

# 9. Dictionary
person = {
    "name": "Umair",
    "age": 25,
    "city": "Newcastle"
}
print("Dictionary:", person, "| Type:", type(person))



