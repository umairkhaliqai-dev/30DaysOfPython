# Unpacking List Items
# Assigning list items to multiple variables at once.
fruits = ['apple', 'banana', 'orange']
fruit1, fruit2, fruit3 = fruits
print(fruit1)  # apple
print(fruit2)  #    banana
print(fruit3)  #   orange
print()
# Slicing Items from a List
# Extracting a portion of a list using [start:end] (end index is exclusive).
print()
fruits = ['apple', 'banana', 'orange', 'mango', 'grape']
print(fruits[1:4])  # ['banana', 'orange', 'mango']
print(fruits[:3])   #  ['apple', 'banana', 'orange']
print(fruits[2:])   #  ['orange', 'mango', 'grape']
